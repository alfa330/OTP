"""ИИ-помощник формулировки разбора (прецедента) для RAG.

Проверяющий пишет причину исправления свободным текстом — этого мало: оценщик потом
может понять правило слишком широко (оправдать чужие нарушения) или слишком узко
(проигнорировать). Этот модуль просит сильную модель переформулировать разбор в
точное правило с явными границами. Результат — ТОЛЬКО ПОДСКАЗКА: человек редактирует
и сохраняет сам, решение всегда за ним."""
from __future__ import annotations

from .. import config
from .. import llm

_SCHEMA = {
    "type": "object",
    "properties": {
        "situation": {"type": "string"},
        "rule": {"type": "string"},
        "not_covered": {"type": "string"},
        "note_to_reviewer": {"type": "string"},
    },
    "required": ["situation", "rule", "not_covered", "note_to_reviewer"],
    "additionalProperties": False,
}

_SYSTEM = """Ты помогаешь контролёру качества звонков отдела продаж сформулировать «разбор» — прецедент, \
который ИИ-оценщик будет применять к похожим ситуациям в будущих звонках. Цель системы — реально ловить \
некомпетентность операторов, поэтому правило не должно давать ИИ повода прощать нарушения, которых человек не прощал.

Преврати свободное объяснение проверяющего в точную формулировку. НЕ меняй смысл решения человека — только уточняй \
и очерчивай границы. Пиши по-русски, коротко и без канцелярита.

Верни строго:
- situation: 1–2 предложения — в какой ситуации правило применяется (обобщённо, без имён клиентов и названий из конкретного звонка).
- rule: правило для оценщика в императивной форме с явным вердиктом: что считать / НЕ считать нарушением по этому критерию и почему.
- not_covered: границы — какие проблемы это правило НЕ оправдывает (чтобы оценщик не распространил его на другие нарушения: \
например «не оправдывает перепутанное имя клиента, искажённое название компании, отсутствие приветствия целиком»). Пункты через «;».
- note_to_reviewer: короткая подсказка проверяющему — что ты уточнил; если его объяснение можно было понять слишком широко, \
двусмысленно или оно противоречит тексту критерия — скажи об этом прямо."""


def refine_adjudication(*, direction_id, criterion_idx, criterion_name=None,
                        ai_verdict=None, ai_comment=None,
                        correct_verdict, reason, excerpt=None) -> dict:
    """Предложение точной формулировки разбора. Возвращает
    {situation, rule, not_covered, note_to_reviewer}. Ошибки БД/LLM пробрасываются (роут → 500)."""
    # Текст критерия из шкалы направления — чтобы правило не противоречило самому критерию.
    from ..evaluation import criteria as criteria_mod
    from ..evaluation.evaluator import _criteria_block
    d = criteria_mod.load_direction(direction_id)
    match = [c for c in d["criteria"] if c["idx"] == int(criterion_idx)]
    crit_text = _criteria_block(match) if match else (criterion_name or f"критерий {criterion_idx}")

    user = (f"КРИТЕРИЙ:\n{crit_text}\n\n"
            f"СИТУАЦИЯ ИЗ ЗВОНКА (фрагмент/цитата):\n{excerpt or '(не приложена)'}\n\n"
            f"ВЕРДИКТ ИИ: {ai_verdict or '—'}"
            + (f"\nОБОСНОВАНИЕ ИИ: {ai_comment}" if ai_comment else "") + "\n\n"
            f"РЕШЕНИЕ ПРОВЕРЯЮЩЕГО: {correct_verdict}\n"
            f"ОБЪЯСНЕНИЕ ПРОВЕРЯЮЩЕГО: {reason or '(не написано — сформулируй из ситуации и решения)'}")
    return llm.claude_json(model=config.CLAUDE_MODEL_HARD, system=_SYSTEM, user=user,
                           schema=_SCHEMA, max_tokens=2000, timeout=60.0)
